renderGames();

function renderGames() {

    matches.forEach(match => {

        if(match.status !== "TIMED") return;

        const date = new Date(match.utcDate)
            .toLocaleString("pt-BR");

        const card = document.createElement("div");

        card.className = "game-card";

        card.innerHTML = `
            <div class="match-date">
                ${date}
            </div>

            <div class="teams">

                <div class="team">
                    ${match.homeTeam}
                </div>

                <div class="score-input">
                    <input
                        type="number"
                        min="0"
                        id="home-${match.id}"
                    >

                    <span>X</span>

                    <input
                        type="number"
                        min="0"
                        id="away-${match.id}"
                    >
                </div>

                <div class="team">
                    ${match.awayTeam}
                </div>

            </div>
        `;

        container.appendChild(card);
    });

}

document
.getElementById("bolaoForm")
.addEventListener("submit", e => {

    e.preventDefault();

    const palpites = matches
        .filter(match => match.status === "TIMED")
        .map(match => {

            return {

                matchId: match.id,

                homeTeam: match.homeTeam,

                awayTeam: match.awayTeam,

                homeGoals: Number(
                    document.getElementById(
                        `home-${match.id}`
                    ).value
                ),

                awayGoals: Number(
                    document.getElementById(
                        `away-${match.id}`
                    ).value
                )
            };

        });

    localStorage.setItem(
        "palpites",
        JSON.stringify(palpites)
    );

    document
        .getElementById("savedPredictions")
        .textContent =
            JSON.stringify(
                palpites,
                null,
                2
            );

    alert("Palpites salvos!");
});

loadPredictions();

function loadPredictions(){

    const saved =
        localStorage.getItem("palpites");

    if(!saved) return;

    const palpites =
        JSON.parse(saved);

    palpites.forEach(p => {

        const home =
            document.getElementById(
                `home-${p.matchId}`
            );

        const away =
            document.getElementById(
                `away-${p.matchId}`
            );

        if(home) home.value =
            p.homeGoals;

        if(away) away.value =
            p.awayGoals;
    });

    document
        .getElementById("savedPredictions")
        .textContent =
            JSON.stringify(
                palpites,
                null,
                2
            );
}

